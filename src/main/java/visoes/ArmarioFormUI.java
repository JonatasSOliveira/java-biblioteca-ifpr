    private final Armario armarioEmEdicao;

    public ArmarioFormUI(Armario armarioEmEdicao) {
        super();
        initComponents();
        super.iniciarComponentes();

        if (armarioEmEdicao == null) {
            this.armarioEmEdicao = new Armario();
            return;
        }
        
        this.inputNumero.setText(armarioEmEdicao.getNumero());
        this.inputObs.setText(armarioEmEdicao.getObservacoes());
        this.armarioEmEdicao = armarioEmEdicao;
    }

    @Override
    protected JPanel getPanelForm() {
        return this.panelForm;
    }

    @Override
    protected void salvar() {
        Armario armario = this.armarioEmEdicao;
        
        armario.setNumero(this.inputNumero.getText());
        armario.setObservacoes(this.inputObs.getText());
        
        if (armario.getId() != null) {
            ArmarioControle.atualizar(armario);
        } else {
            ArmarioControle.criar(armario);
        }
    }

