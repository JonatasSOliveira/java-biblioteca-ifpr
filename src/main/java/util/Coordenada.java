package util;

public class Coordenada {
    private int pontoX;
    private int pontoY;

    public Coordenada(int pontoX, int pontoY) {
        this.pontoX = pontoX;
        this.pontoY = pontoY;
    }

    public int getPontoX() {
        return pontoX;
    }

    public int getPontoY() {
        return pontoY;
    }
}
